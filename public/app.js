function formatPrice(preco) {
  return "R$ " + preco.toFixed(2).replace(".", ",");
}

function renderStars(avaliacao) {
  var cheias  = Math.floor(avaliacao / 2);
  var meia    = (avaliacao / 2 - cheias) >= 0.5 ? 1 : 0;
  var vazias  = 5 - cheias - meia;
  var html    = "";
  for (var i = 0; i < cheias; i++)  html += "★";
  if (meia)                          html += "½";
  for (var i = 0; i < vazias; i++)  html += "☆";
  return html;
}

function renderCategories() {
  var categorySelect = document.querySelector("#category");
  if (!categorySelect) return;

  var categorias = [];
  for (var i = 0; i < data.produtos.length; i++) {
    var cat = data.produtos[i].categoria;
    if (categorias.indexOf(cat) === -1) {
      categorias.push(cat);
    }
  }

  for (var i = 0; i < categorias.length; i++) {
    var option = document.createElement("option");
    option.setAttribute("value", categorias[i]);
    option.innerHTML = categorias[i];
    categorySelect.appendChild(option);
  }
}

function createProductCard(produto) {
  var col = document.createElement("div");
  col.setAttribute("class", "col");

  var estoqueClass  = produto.emEstoque ? "text-success" : "text-danger";
  var estoqueTxt    = produto.emEstoque ? "Em estoque" : "Fora de estoque";
  var badgeClass    = produto.emEstoque ? "bg-success" : "bg-danger";

  col.innerHTML = `
    <div class="card h-100 shadow-sm game-card" data-id="${produto.id}">
      <div class="position-relative">
        <img src="${produto.imagem}" class="card-img-top game-img" alt="${produto.nome}">
        <span class="badge ${badgeClass} position-absolute top-0 end-0 m-2">${estoqueTxt}</span>
      </div>
      <div class="card-body d-flex flex-column">
        <span class="badge bg-secondary mb-2 align-self-start">${produto.categoria}</span>
        <h5 class="card-title">${produto.nome}</h5>
        <p class="card-text small flex-grow-1">${produto.descricao}</p>
        <div class="mt-auto">
          <p class="fw-bold fs-5 mb-2">${formatPrice(produto.preco)}</p>
          <div class="d-flex gap-2">
            <a href="detalhes.html?id=${produto.id}" class="btn btn-primary btn-sm flex-grow-1">
              Ver detalhes
            </a>
            <button class="btn btn-outline-warning btn-sm btn-destacar" data-id="${produto.id}">
              ★
            </button>
          </div>
        </div>
      </div>
    </div>
  `;

  col.querySelector(".btn-destacar").addEventListener("click", function() {
    var card = col.querySelector(".game-card");
    card.classList.toggle("highlight");
  });

  return col;
}


function renderProducts(produtos) {
  var productList = document.getElementById("product-list");
  if (!productList) return;

  productList.innerHTML = "";

  if (produtos.length === 0) {
    productList.innerHTML = `
      <div class="col-12 text-center py-5 text-muted">
        <p class="fs-5">Nenhum jogo encontrado.</p>
      </div>`;
    return;
  }

  for (var i = 0; i < produtos.length; i++) {
    productList.appendChild(createProductCard(produtos[i]));
  }
}


function filterProducts() {
  var searchInput   = document.querySelector("#search");
  var categorySelect = document.querySelector("#category");
  if (!searchInput || !categorySelect) return data.produtos;

  var texto     = searchInput.value.toLowerCase();
  var categoria = categorySelect.value;
  var resultado = [];

  for (var i = 0; i < data.produtos.length; i++) {
    var produto = data.produtos[i];
    var nomeOk  = produto.nome.toLowerCase().includes(texto);
    var catOk   = categoria === "Todas" || produto.categoria === categoria;
    if (nomeOk && catOk) resultado.push(produto);
  }

  return resultado;
}

